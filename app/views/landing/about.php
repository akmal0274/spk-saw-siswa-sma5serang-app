        <p>
            SMA Negeri 5 yang dulunya bernama SMA Bina Taruna berdiri sejak tahun 1979, dipelopori oleh guru-guru SMAN 1 Serang. SMA tersebut di bawah naungan yayasan Bina Taruna. 
            Pada tanggal 20 Juni 2006 seluruh aset SMA Bina Taruna yang berupa gedung, guru tetap, guru honorer, dan para siswa dihibahkan ke PEMDA Serang (saat itu masih kabupaten). 
            Dan pada tanggal 30 Mei 2007 secara resmi berubah nama menjadi SMA Negeri 2 Serang. Pada tahun 2007 terjadi pemekaran wilayah Serang menjadi 2 wilayah, yakni Kabupaten Serang dan Kota Serang. 
            Sehubungan dengan adanya pemekaran wilayah, maka nama SMAN 2 Serang pun ikut berubah menjadi SMA Negeri 5 KOTA SERANG.
        </p>
